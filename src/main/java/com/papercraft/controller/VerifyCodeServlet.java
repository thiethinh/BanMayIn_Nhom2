package com.papercraft.controller;

import com.papercraft.dao.UserDAO;
import com.papercraft.model.User;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.servlet.http.HttpSession;

import java.io.IOException;

@WebServlet(name = "VerifyCodeServlet", value = "/verify-code")
public class VerifyCodeServlet extends HttpServlet {
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        String inputOTP = request.getParameter("otp");

        HttpSession session = request.getSession();
        String serverOTP = (String) session.getAttribute("authCode");
        User tempUser = (User) session.getAttribute("tempUser");

        if (serverOTP == null || tempUser == null) {
            request.setAttribute("msg", "Phiên giao dịch hết hạn vui lòng đăng ký lại");
            request.getRequestDispatcher("login.jsp").forward(request, response);
            return;
        }

        if (inputOTP.equals(serverOTP)) {
            UserDAO dao = new UserDAO();
            dao.signup(tempUser);

            session.removeAttribute("authCode");
            session.removeAttribute("tempUser");
            session.setAttribute("acc",  tempUser);
            request.setAttribute("msg", "Đăng ký thành công! Bạn có thể đăng nhập");

            String redirectUrl = (String) session.getAttribute("redirectAfterRegister");
            session.removeAttribute("redirectAfterRegister");
            if (redirectUrl != null && !redirectUrl.trim().isEmpty()) {
                response.sendRedirect(redirectUrl);
            } else {
                response.sendRedirect("home");
            }
        } else {
            request.setAttribute("errorVerify", "Mã OTP không đúng");
            request.setAttribute("showVerifyModal", true);
            request.setAttribute("activeTab", "register");
            request.getRequestDispatcher("login.jsp").forward(request, response);
        }
    }
}
